export const LANGS = [
  { code: 'es', label: 'Español',  flag: '🇪🇸' },
  { code: 'en', label: 'English',  flag: '🇬🇧' },
  { code: 'fr', label: 'Français', flag: '🇫🇷' },
  { code: 'de', label: 'Deutsch',  flag: '🇩🇪' },
  { code: 'it', label: 'Italiano', flag: '🇮🇹' },
]

const _supported = LANGS.map(l => l.code)
const _stored = localStorage.getItem(STORAGE_KEY)
let _locale = _supported.includes(_stored) ? _stored : 'es'

export function getLocale() { return _locale }

export function setLocale(lang) {
  if (!_supported.includes(lang) || _locale === lang) return
  _locale = lang
  localStorage.setItem(STORAGE_KEY, lang)
  window.dispatchEvent(new CustomEvent('locale-changed', { detail: lang }))
}

export function t(key) {
  return translations[_locale]?.[key] ?? translations['es'][key] ?? key
}

/**
 * Mixin that makes a LitElement re-render automatically on locale changes.
 */
export const I18nMixin = (Base) => class extends Base {
  connectedCallback() {
    super.connectedCallback()
    this.__onLocaleChange = () => this.requestUpdate()
    window.addEventListener('locale-changed', this.__onLocaleChange)
  }
  disconnectedCallback() {
    super.disconnectedCallback()
    window.removeEventListener('locale-changed', this.__onLocaleChange)
  }
}
